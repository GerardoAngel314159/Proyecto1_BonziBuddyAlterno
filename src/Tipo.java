/**
 * Enumeracion que define los posibles tipos de componentes disponibles.
 */
public enum Tipo {
    
    /**
     * Representa un procesador.
     */
    PROCESADOR,
    
    /**
     * Representa una memoria RAM.
     */
    MEMORIA_RAM,
    
    /**
     * Representa una unidad de estado solido (SSD).
     */
    SSD,
    
    /**
     * Representa un disco duro (HDD).
     */
    HDD,
    
    /**
     * Representa una unidad de procesamiento grafico (GPU).
     */
    GPU,
    
    /**
     * Representa una placa base (Motherboard).
     */
    MOTHERBOARD,
    
    /**
     * Representa una fuente de alimentacion.
     */
    FUENTE_ALIMENTACION,
    
    /**
     * Representa un gabinete.
     */
    GABINETE;
}